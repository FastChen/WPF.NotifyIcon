namespace WPF.NotifyIcon
{
    public enum ToolTipIcon
    {
        /// <summary>
        /// 不显示图标
        /// No icon.
        /// </summary>
        None = 0,

        /// <summary>
        /// 信息图标
        /// Info icon.
        /// </summary>
        Info = 1,

        /// <summary>
        /// 警告图标
        /// Warning icon.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// 错误图标
        /// Error icon.
        /// </summary>
        Error = 3,

        /// <summary>
        /// 创建时使用应用程序图标
        /// Use Application icon.
        /// </summary>
        Custom = 4,
    }
}
